
function myFunction() {
    var x = document.getElementById("display");
    var y = document.getElementById("session");
    var x1 = document.getElementById("display1");
    var y1 = document.getElementById("session1");
    var x2 = document.getElementById("display2");
    var y2 = document.getElementById("session2");
    var x3 = document.getElementById("display3");
    var y3 = document.getElementById("session3");
    if (x.style.display === "none") {
      x.style.display = "block";
      y.style.backgroundColor = "#FFCC00"
      x1.style.display = "none";
      y1.style.backgroundColor = "#FFF9E1"
      x2.style.display = "none";
      y2.style.backgroundColor = "#FFF9E1"
      x3.style.display = "none";
      y3.style.backgroundColor = "#FFF9E1"
    } else {
      x.style.display = "none";
      y.style.backgroundColor = "#FFF9E1"
    }
  }

